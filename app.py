def add_numbers(a, b):
    """Add two numbers together"""
    return a + b

def subtract_numbers(a, b):
    """Subtract b from a"""
    return a - b

if __name__ == "__main__":
    print("Welcome to my simple math app!")
    print("2 + 3 =", add_numbers(2, 3))
    print("5 - 2 =", subtract_numbers(5, 2))